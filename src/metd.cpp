//-----------------------------------------------------------------------------
// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------

double metd(std::vector<double>& inputs,
            int first=0,
            int last=metd_::nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = metd_::nnetworks-1;
  if ( last  > metd_::nnetworks-1 ) last = metd_::nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** metd ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(metd_::ninputs);
  in[0] = (inputs[0] - metd_::mean[0]) / metd_::sigma[0];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += metd_::netfun(in, metd_::weight[i]);
  x /= n;
  return x;
}

double metd(double f_pfmet,
            int first=0, int last=metd_::nnetworks-1)
{
  std::vector<double> in(metd_::ninputs);
  in[0] = f_pfmet;
  return metd(in, first, last);
}
